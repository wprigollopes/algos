#include <bits/stdc++.h>

using namespace std;

class Node {
    public:
        int data;
        Node *left;
        Node *right;
        Node(int d) {
            data = d;
            left = NULL;
            right = NULL;
        }
};

class Solution {
    public:
  		Node* insert(Node* root, int data) {
            if(root == NULL) {
                return new Node(data);
            } else {
                Node* cur;
                if(data <= root->data) {
                    cur = insert(root->left, data);
                    root->left = cur;
                } else {
                    cur = insert(root->right, data);
                    root->right = cur;
               }

               return root;
           }
        }
/*The tree node has data, left child and right child 
class Node {
    int data;
    Node* left;
    Node* right;
};

*/
    int height(Node* root) {
        int depth = 0;
        deque<Node*> depths;
        depths.push_back(root);

        while (depths.size()>0) {
            int size = depths.size();
            for (int i=0; i<size; i++) {
                Node* curr = depths.front();
                depths.pop_front();
                if (curr->left != NULL) {
                    depths.push_back(curr->left);
                }
                if (curr->right != NULL) {
                    depths.push_back(curr->right);
                }
            }
            depth++;
        }
        return depth;
    }

}; //End of Solution

